export const convertToMorse = (str) => {
    return str.toUpperCase().split("").map(el => {
        return morseCode[el] ? morseCode[el] : el;   
    }).join("/");
};

export const convertToEnglish = (str) => {
    return str.split("/").map(el => {
        return english[el] ? english[el] : el;   
    }).join("");
};

